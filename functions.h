#include <iostream>

using std::cout;
using std::cin;
using std::endl;

double Sum(double x, double y)
{
    double sum = x + y;
    cout << sum << endl;
}

double Subtract(double x, double y)
{
    double sub = x - y;
    cout << sub << "\n";
}

double Divide(double x, double y)
{
    double div = x/y;
    cout << div << "\n";
}

double Multiply(double x, double y)
{
    double mult = x * y;
    cout << mult << "\n";
}